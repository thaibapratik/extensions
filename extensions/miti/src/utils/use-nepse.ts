export function useNepseStocks() {
  const [favorites, setFavorites] = useCachedState<string[]>(
    "nepse-favorites-v2",
    [],
  );

  const { data: marketData, isLoading } = useCachedPromise(
    async () => {
      try {
        const [res, indexRes, summaryRes] = await Promise.all([
          fetch("https://merolagani.com/LatestMarket.aspx"),
          fetch("https://merolagani.com/Indices.aspx"),
          fetch(
            "https://merolagani.com/handlers/webrequesthandler.ashx?type=market_summary",
          ),
        ]);

        const html = await res.text();
        const $ = cheerio.load(html);

        const stocks: Record<
          string,
          Omit<StockData, "symbol" | "name" | "isFavorite">
        > = {};
        $("table.table-hover tbody tr").each((i, el) => {
          const symbol = $(el).find("td:nth-child(1)").text().trim();
          const ltp = $(el).find("td:nth-child(2)").text().trim();
          const percentageChange = $(el).find("td:nth-child(3)").text().trim();
          const open = $(el).find("td:nth-child(4)").text().trim();
          const high = $(el).find("td:nth-child(5)").text().trim();
          const low = $(el).find("td:nth-child(6)").text().trim();
          const qty = $(el).find("td:nth-child(7)").text().trim();

          if (symbol && symbol !== "Promotor Share") {
            let pointChange = "0.00";
            if (percentageChange && ltp) {
              const p = parseFloat(percentageChange);
              const l = parseFloat(ltp.replace(/,/g, ""));
              if (!isNaN(p) && !isNaN(l) && p !== 0) {
                const diff = (l * p) / (100 + p);
                pointChange = diff.toFixed(2);
                if (pointChange.startsWith("-") === false && p > 0) {
                  pointChange = "+" + pointChange;
                }
              }
            }
            stocks[symbol] = {
              ltp,
              percentageChange,
              open,
              high,
              low,
              qty,
              pointChange,
            };
          }
        });

        const indexHtml = await indexRes.text();
        const $index = cheerio.load(indexHtml);
        const firstRow = $index("table tr").eq(1).find("td");
        const indexData: IndexData = {
          index: firstRow.eq(2).text().trim(),
          change: firstRow.eq(3).text().trim(),
          percentageChange: firstRow.eq(4).text().trim(),
          date: firstRow.eq(1).text().trim(),
        };

        const summaryJson = (await summaryRes.json()) as {
          overall: { t: string; q: string; tn: string; mc: string };
        };
        const summary: MarketSummary = {
          turnover: summaryJson.overall.t,
          volume: summaryJson.overall.q,
          transactions: summaryJson.overall.tn,
          marketCap: summaryJson.overall.mc,
        };

        return { stocks, indexData, summary };
      } catch (e) {
        console.error("Failed to fetch NEPSE data", e);
        return { stocks: {}, indexData: null, summary: null };
      }
    },
    [],
    {
      keepPreviousData: true,
    },
  );

  const data = useMemo(() => {
    const rawData = marketData?.stocks;
    if (!rawData) {
      return STOCK_LIST.map((stock) => ({
        ...stock,
        ltp: "-",
        pointChange: "-",
        percentageChange: "-",
        high: "-",
        low: "-",
        open: "-",
        qty: "-",
        isFavorite: favorites.includes(stock.symbol),
      })).sort((a, b) => {
        if (a.isFavorite && !b.isFavorite) return -1;
        if (!a.isFavorite && b.isFavorite) return 1;
        return 0;
      });
    }

    return STOCK_LIST.map((stock) => {
      const live = (
        rawData as Record<
          string,
          Omit<StockData, "symbol" | "name" | "isFavorite">
        >
      )[stock.symbol];
      if (live) {
        return {
          ...stock,
          ltp: live.ltp || "-",
          pointChange: live.pointChange || "-",
          percentageChange: live.percentageChange || "-",
          high: live.high || "-",
          low: live.low || "-",
          open: live.open || "-",
          qty: live.qty || "-",
          isFavorite: favorites.includes(stock.symbol),
        };
      }
      return {
        ...stock,
        ltp: "-",
        pointChange: "-",
        percentageChange: "-",
        high: "-",
        low: "-",
        open: "-",
        qty: "-",
        isFavorite: favorites.includes(stock.symbol),
      };
    }).sort((a, b) => {
      if (a.isFavorite && !b.isFavorite) return -1;
      if (!a.isFavorite && b.isFavorite) return 1;
      return 0;
    });
  }, [marketData, favorites]);

  const toggleFavorite = (symbol: string) => {
    setFavorites((prev) => {
      if (prev.includes(symbol)) {
        return prev.filter((s) => s !== symbol);
      }
      return [...prev, symbol];
    });
  };

  return {
    data,
    indexData: marketData?.indexData,
    marketSummary: marketData?.summary,
    isLoading,
    toggleFavorite,
  };
}
